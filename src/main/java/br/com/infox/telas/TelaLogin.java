    public void logar() {
        String sql = "select * from tbusuarios where login = ? and senha = ?";
        try {
            /* AS linhas abaixo preparam a consulta ao banco em função do que foi
        digitado nas caixas de texto. O '?' é substituído pelo conteúdo das variáveis*/
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtUsuario.getText());
            String captura = new String(txtSenha.getPassword());
            pst.setString(2, captura);

            // A linha abaixo executa a consulta ao banco de dados(query)
            rs = pst.executeQuery();

            // Se existir um usuário e senha correspondente
            if (rs.next()) {
                // A linha abaixo obtem o conteúdo do campo 'perfil' da tabela tbusuarios
                String perfil = rs.getString(6);

                // A estrutura abaixo faz o tratamento do perfil do usuário
                if (perfil.equals("admin")) {
                    TelaPrincipal principal = new TelaPrincipal();
                    principal.setVisible(true);
                    TelaPrincipal.menuRelatorio.setEnabled(true);
                    TelaPrincipal.menuCadastroUsuario.setEnabled(true);
                    TelaPrincipal.lblUsuario.setText(rs.getString(2));
                    TelaPrincipal.lblUsuario.setForeground(Color.red);
                    this.dispose();
                } else {
                    TelaPrincipal principal = new TelaPrincipal();
                    principal.setVisible(true);
                    TelaPrincipal.lblUsuario.setText(rs.getString(2));
                    this.dispose();
                }
            } else {
                JOptionPane.showMessageDialog(null, "usuário e/ou senha inválido(s)");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

